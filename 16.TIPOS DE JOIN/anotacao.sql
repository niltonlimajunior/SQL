--INNER JOIN

--Retorna apneas os resultados que correspondem (existem) tanto na tabela A como Tabela B

--FULL OUTER JOIN

--Retorna um conjunto de todos registros correspondentes da TabelaA e TabelaB quando são iguais. E além disso se não houver valores correspondentes, ele simplesmente irá preencher esse lado com "null"

--LEFT OUTER JOIN

--Retorna um conjunto de todos os registros da TabaleA, e além disso, os registros correspondentes(quando disponíveis) na TabelaB. Se não houver registro correspondente ele simplesmente vai preencher com "null"
